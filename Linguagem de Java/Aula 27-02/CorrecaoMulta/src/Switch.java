public class Switch {
  public static void main(String[] args){
    int nota = 5;

    switch (nota) {
        case 1:
        System.out.println("mto ruim");
        break;
        case 2:
          System.out.println("ruim");
          break;
        case 3:
          System.out.println("bom");
          break;
        case 4:
          System.out.println("otimo");
          break;
        default:
          System.out.println("otimo");
          break;
          
    }
  }
}
